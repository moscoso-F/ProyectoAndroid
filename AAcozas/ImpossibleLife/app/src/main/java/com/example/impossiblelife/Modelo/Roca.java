package com.example.impossiblelife.Modelo;

public class Roca {

    float varX, varY, ancho, alto;

    public Roca(float varX, float varY, float ancho, float alto) {
        this.varX = varX;
        this.varY = varY;
        this.ancho = ancho;
        this.alto = alto;
    }

    public float getVarX() {
        return varX;
    }

    public void setVarX(float varX) {
        this.varX = varX;
    }

    public float getVarY() {
        return varY;
    }

    public void setVarY(float varY) {
        this.varY = varY;
    }

    public float getAncho() {
        return ancho;
    }

    public void setAncho(float ancho) {
        this.ancho = ancho;
    }

    public float getAlto() {
        return alto;
    }

    public void setAlto(float alto) {
        this.alto = alto;
    }

}
